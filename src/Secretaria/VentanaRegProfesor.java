    private void cargarDatosDesdeBaseDeDatos() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);

        try (Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD)) {
            String sql = "SELECT ID_PROFESORES, NOMBREPROFESOR, CARNETPROFESOR, DOMICILIOPROFESOR, TELEFONOPROFESOR, CELULARPROFESOR FROM profesores";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);

            while (rs.next()) {
                int id = rs.getInt("ID_PROFESORES");
                String nombre = rs.getString("NOMBREPROFESOR");
                String carnet = rs.getString("CARNETPROFESOR");
                String domicilio = rs.getString("DOMICILIOPROFESOR");
                String telefonoFijo = rs.getString("TELEFONOPROFESOR");
                String telefonoCelular = rs.getString("CELULARPROFESOR");

                modelo.addRow(new Object[]{id, nombre, carnet, domicilio, telefonoFijo, telefonoCelular});
            }
        } catch (SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error al cargar datos: " + e.getMessage());
        }
    }

