    private void limpiarCampos() {
    jTextField1.setText("");  // Nombre
    jTextField2.setText("");  // Carnet
    jTextField3.setText("");  // Domicilio
    jTextField4.setText("");  // Teléfono Fijo
    jTextField5.setText("");  // Teléfono Celular
}
    private boolean validarDatos() {
    String telefonoFijo = jTextField4.getText().trim();
    String telefonoCelular = jTextField5.getText().trim();

    // Validación de teléfono fijo
    if (!telefonoFijo.isEmpty() && !telefonoFijo.matches("\\d{7}")) {
        javax.swing.JOptionPane.showMessageDialog(this, "El teléfono fijo debe tener exactamente 7 dígitos.");
        return false;
    }

    // Validación de teléfono celular
    if (!telefonoCelular.isEmpty() && !telefonoCelular.matches("\\d{8}")) {
        javax.swing.JOptionPane.showMessageDialog(this, "El teléfono celular debe tener exactamente 8 dígitos.");
        return false;
    }

    return true;
}


    
