    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = jTable1.getSelectedRow();

    if (filaSeleccionada >= 0) {
        int idProfesor = (int) jTable1.getValueAt(filaSeleccionada, 0);

        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this, 
            "¿Estás seguro de que deseas eliminar este profesor?", 
            "Confirmar eliminación", 
            javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
            try (Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD)) {
                conn.setAutoCommit(false); // Desactivar autocommit para asegurar la transacción

                // 1️⃣ Eliminar registros relacionados en horarios_profesores
                String sqlHorarios = "DELETE FROM horarios_profesores WHERE ID_PROFESORES = ?";
                try (PreparedStatement pstmtHorarios = conn.prepareStatement(sqlHorarios)) {
                    pstmtHorarios.setInt(1, idProfesor);
                    pstmtHorarios.executeUpdate();
                }

                // 2️⃣ Ahora eliminar al profesor
                String sqlProfesor = "DELETE FROM profesores WHERE ID_PROFESORES = ?";
                try (PreparedStatement pstmtProfesor = conn.prepareStatement(sqlProfesor)) {
                    pstmtProfesor.setInt(1, idProfesor);
                    int filasAfectadas = pstmtProfesor.executeUpdate();

                    if (filasAfectadas > 0) {
                        javax.swing.JOptionPane.showMessageDialog(this, "Profesor eliminado exitosamente.");
                        conn.commit(); // Confirmar la transacción
                        cargarDatosDesdeBaseDeDatos(); // Actualizar la tabla
                    } else {
                        javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar el profesor.");
                        conn.rollback(); // Revertir cambios si algo falla
                    }
                }
            } catch (SQLException e) {
                e.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(this, "Error de base de datos: " + e.getMessage());
            }
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecciona un profesor para eliminar.");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (!validarDatos()) return; // Detener el proceso si la validación falla

    String nombre = jTextField1.getText().trim();
    String carnet = jTextField2.getText().trim();
    String domicilio = jTextField3.getText().trim();
    String telefono = jTextField4.getText().trim();
    String celular = jTextField5.getText().trim();

    if (nombre.isEmpty() || carnet.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "El nombre y el carnet son obligatorios.");
        return;
    }

    String usuario = nombre.replaceAll("\\s", "").substring(0, 5) + "005";
    String contrasena = nombre.split(" ")[0] + "005";

    try (Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD)) {
        String sql = "INSERT INTO profesores (NOMBREPROFESOR, CARNETPROFESOR, DOMICILIOPROFESOR, TELEFONOPROFESOR, CELULARPROFESOR, USUARIO, CONTRASEÑA) VALUES (?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, nombre);
        ps.setString(2, carnet);
        ps.setString(3, domicilio.isEmpty() ? null : domicilio);
        ps.setString(4, telefono.isEmpty() ? null : telefono);
        ps.setString(5, celular.isEmpty() ? null : celular);
        ps.setString(6, usuario);
        ps.setString(7, contrasena);

        int filasAfectadas = ps.executeUpdate();
        if (filasAfectadas > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Profesor añadido correctamente.");
            cargarDatosDesdeBaseDeDatos();
            limpiarCampos();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al añadir profesor.");
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Error de base de datos: " + ex.getMessage());
    }
    }//GEN-LAST:event_jButton4ActionPerformed

